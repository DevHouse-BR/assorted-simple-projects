<h1>Spiceee</h1>

<p>Baseado em São Paulo desde 2000, Spiceee é um dos responsáveis pelo fomento da cena eletrônica em Florianópolis (Santa Catarina).</p>

<p>Estreou como DJ em 1996 mixando seus discos no Carnaval Tecno do bar Órbita, festa de quatro dias que foi o marco do início da cena na região. </p>

<p>Em 2001, Spiceee viu o reconhecimento do seu trabalho em São Paulo quando foi indicado na categoria DJ revelação da premiação da coluna Noite Ilustrada, da Folha de S.Paulo.</p>

<p>Em 99, a agência Subtech, atuante no leste europeu, gostou dos seus sets. O resultado foi uma turnê por Viena (Áustria) e Bratislava (Eslováquia), cuja gravação do set acabou nas mãos da agência Disko B/Gigolo Bookings, de nomes como DJ Hell e Jay Denham, e resultou em outra turnê pela Europa, em agosto de 2000, desta vez por Munique (Alemanha) e Sarajevo (Bósnia), onde Spiceee tocou para 12 mil pessoas.</p>







<h1>Ledgroove</h1>

<p>Com uma mistura irresistível de disco, hi energy, house music, italo disco, grooves cósmicos, Ledgroove cria a seu modo, uma mistura despretensiosa de elementos dançantes. Produtor de mão cheia, com lançamentos bem recebidos em um dos selos brasileiros mais respeitados na atualidade, o MisterMistery records, aos poucos vem chamando a atenção no cenário nacional. Suas músicas são constantemente tocadas por djs como Márcio Vermelho, Victor A aka Rotciv, Gustavo Pamplona, Leiloca Pantoja, Davis, JotaJota, Benjamin Ferreira, e Hubert, e nas pistas de festas como a Freak Chiq (d-edge SP), LUDO (Vegas SP), SOM (Blues Velvet, Floripa), Noize (Juiz de Fora), Slow Dance Society, e Sounds in The City, em Floripa. </p>

<p>O produtor começou sua carreira no final da década de 90, quando o techno encontrava-se em um momento de grande criatividade, fascinando o dj pelo estilo, que passou divulgá-lo em sua cidade natal, Florianópolis. Criou então um grupo de festas denominado FLUNDI (Floripa Underground Information), que chamou a atenção de mídias como o Diário Catarinense, e de público formador de opinião na cidade. A partir dai foram surgindo os convites, e a primeira residência, no Conceição nite, onde teve noites memoráveis ao lado de djs já consagrados como Luiz Pareto, Marcos Morcerf, Maumau, e Leozinho. Participou de outros projetos vanguardistas como a Repulsiva, Devassa, e a Less, todos em Florianópolis, sempre divulgando e tentando abrir os ouvidos de promessas de novos sons. Já foi agenciado pela VIBE'S djs, e tocou em grandes eventos, como a tenda E-planet, do Planeta Altântida. </p>

<p>Também produziu a trilha do espetáculo premiado, e patrocinado pela PETROBRAS, “Hipo-teses para o corpo”, de autoria do coreógrafo e bailarino Volmir Cordeiro, em uma experiência excitante de abstração e liberdade total de criação. No ano de 2010, morou em São Paulo, onde pode conviver com produtores, djs, e toda a cena house/nudisco paulistana, que serviu como laboratório para novas criações. Já ministrou aulas de discotecagem, e hoje em dia dedica-se com a devoção ao estúdio de produção. </p>

<p>Além de ser um dos artistas do cast MisterMistery, para 2011 Ledgroove tem agendado seu primeiro release no selo “Rainbow Socks”, do dj e produtor Pejota Fernandes. Também pretende lançar suas inúmeras faixas até então aprisionadas em seu bunker, sob o seu próprio selo, tendo uma proposta mais freestyle, sem a premissa fatalista de obrigatoriamente serem direcionadas a pista. Neste seu mais recente projeto, o Phat Sub Mission, se reflete o momento atual do produtor, influenciado por artistas como Flying Lotus, Brian Eno, Four Tet, Actress, Boards of Canada, Telefón Tel Aviv, Apparat, e pelas batidas mais lentas, sob toneladas de referências ao dub, hip hop, idm, basslines vigorosos, onde a regra é libertar-se totalmente de rótulos, e inserir o máximo possível todas às fases de pesquisa e criação musical. A ideia é criar um álbum (que está em fase de finalização), e a partir dele, um live pa, com inserções de instrumentos e vocais, que particularizam às apresentações ao vivo. </p>

